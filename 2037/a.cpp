using namespace std;
ll M = 998244353;
ll N = 2e5 + 1;

void solve()
{
  ll n; cin >> n;
  vector<ll> v(n);
  map<ll, ll> mp;
  ll ans = 0;
  loop(i, n) {
    cin >> v[i];
    mp[v[i]]++;
    if (mp[v[i]] >= 2) {
      ans++;
      mp[v[i]] = 0;
    }
  }
  cout << ans << endl;
}

int main()
{
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  cout.tie(0);
  ll t; cin >> t;
  while (t--)
    solve();

  return 0;
}
