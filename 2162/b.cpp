void solve()
{
  int n; cin >> n;
  string s; cin >> s;
  vector<int> ans;
  for (int i = 0; i < n; i++) {
    if (s[i] == '0') ans.push_back(i + 1);
  }
  cout << ans.size() << endl;
  for (auto x : ans) cout << x << " ";
  cout << endl;
}

int main()
{
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  cout.tie(0);
  ll t; cin >> t;
  while (t--)
    solve();

  return 0;
}
